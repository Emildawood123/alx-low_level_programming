/**
 * add - that add two numbers
 * @a: first
 * @b: second
 * Return: sum
 */
int add(int a, int b)
{
return (a + b);
}
/**
 * sub - that substract two numbers
 * @a: first
 * @b: second
 * Return: sum
 */
int sub(int a, int b)
{
return (a - b);
}
/**
 * mul - that multi.. two numbers
 * @a: first
 * @b: second
 * Return: sum
 */
int mul(int a, int b)
{
return (a * b);
}
/**
 * div - that division two numbers
 * @a: first
 * @b: second
 * Return: sum
 */
int div(int a, int b)
{
return (a / b);
}
/**
 * mod - that remiund two numbers
 * @a: first
 * @b: second
 * Return: sum
 */
int mod(int a, int b)
{
return (a % b);
}
